// Test ES6 Math methods

function user_main(): number {
    // Math.sign
    console.log("Math.sign(5): " + Math.sign(5));    // 1
    console.log("Math.sign(-5): " + Math.sign(-5));  // -1
    console.log("Math.sign(0): " + Math.sign(0));    // 0

    // Math.trunc
    console.log("Math.trunc(4.7): " + Math.trunc(4.7));   // 4
    console.log("Math.trunc(-4.7): " + Math.trunc(-4.7)); // -4

    return 0;
}
