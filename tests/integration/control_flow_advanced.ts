function main() {
    let x = 2;
    switch (x) {
        case 1:
            console.log(10);
            break;
        case 2:
            console.log(20);
            break;
        default:
            console.log(30);
            break;
    }

    let s = "hello";
    switch (s) {
        case "world":
            console.log(0);
            break;
        case "hello":
            console.log(1);
            break;
    }
}
